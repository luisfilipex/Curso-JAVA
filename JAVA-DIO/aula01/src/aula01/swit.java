package aula01;

public class swit {

	public static void main(String[] args) {
		
		int numero = 5;
		
		
		switch(numero) {
		
		case 1:
			System.out.println("vc digitou 1");
			break;
		case 2:
			System.out.println("vc digitou 2");
		case 3:
			System.out.println("vc digitou 3");
		default:
			System.out.println("Numero invalido");
		}
		
		
			
	}

}
