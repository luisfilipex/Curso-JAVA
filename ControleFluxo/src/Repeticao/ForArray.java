package Repeticao;

public class ForArray {

    public static void main(String[] args) {

        String alunos[] = {"Luis", "Joao", "Maria", "Pedro"};

        for (int x = 0; x < alunos.length; x++) {
            System.out.println("O Aluno no índice " + x + " é " + alunos[x]);
        }
    }
}
